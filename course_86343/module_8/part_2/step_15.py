import sys

DEBUG = False


def set_debug_input():
    input_filename = 'input.txt'
    sys.stdin = open(input_filename, encoding='utf-8')


def main():
    n, m, k, x, y, z, t, a = map(int, (input() for _ in range(8)))
    nm = n + m - x
    mk = m + k - y
    kn = k + n - z
