import sys

DEBUG = False


def set_debug_input():
    input_filename = 'input.txt'
    sys.stdin = open(input_filename, encoding='utf-8')


def main():
    string = input().lower()
    result = {}
    for word in string.split():
        word = word.strip('.,!?:;-')
        result[word] = 1 if word not in result else result[word] + 1
    result = sorted([x for x in result if result[x] == min(result.values())])
    print(result[0])


if __name__ == '__main__':
    if DEBUG:
        set_debug_input()
    main()
